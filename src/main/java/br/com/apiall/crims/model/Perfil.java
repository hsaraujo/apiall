package br.com.apiall.crims.model;

public class Perfil 
{
	private String nome, moral;
	private double respeito, estamina, vicio, inteligencia, carisma, forca, resistencia, grana;
	
	public Perfil(){ }
	
	public double getRespeito(){
		return respeito;
	}
	public void setRespeito(double respeito){
		this.respeito = respeito;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getMoral() {
		return moral;
	}
	public void setMoral(String moral) {
		this.moral = moral;
	}
	public double getEstamina() {
		return estamina;
	}
	public void setEstamina(double estamina) {
		this.estamina = estamina;
	}
	public double getVicio() {
		return vicio;
	}
	public void setVicio(double vicio) {
		this.vicio = vicio;
	}
	public double getInteligencia() {
		return inteligencia;
	}
	public void setInteligencia(double inteligencia) {
		this.inteligencia = inteligencia;
	}
	public double getCarisma() {
		return carisma;
	}
	public void setCarisma(double carisma) {
		this.carisma = carisma;
	}
	public double getForca() {
		return forca;
	}
	public void setForca(double forca) {
		this.forca = forca;
	}
	public double getResistencia() {
		return resistencia;
	}
	public void setResistencia(double resistencia) {
		this.resistencia = resistencia;
	}
	public double getGrana() {
		return grana;
	}
	public void setGrana(double grana) {
		this.grana = grana;
	}
	
	
}
